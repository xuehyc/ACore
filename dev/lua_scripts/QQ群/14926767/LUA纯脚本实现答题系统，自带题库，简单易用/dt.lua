function dt.reset()
    if (dt.id == 0) then
        dt.id = math.random(1, #dt.data)
    end
end

function dt.remind()
    if (dt.id ~= 0) then
        SendWorldMessage('|cFFF08000[答题系统]|r本轮答题已经开始.请使用命令".' .. dt.cmd .. '"参与')
    end
end

function dt.chatcmd(_, p, c)
    if (c == dt.cmd) then
        if dt.id == 0 then
            p:SendBroadcastMessage("本轮答题结束.下一轮请关注聊天窗口.再接再厉！")
        else
            dt.show(p)
        end
        return false
    end
end

function dt.show(p)
    local d = dt.data[dt.id]
    p:GossipClearMenu()
    p:GossipMenuAddItem(0, "本轮题目：\n" .. d[1] .. "", 1, 0)
    for i = 2, 5 do
        p:GossipMenuAddItem(5, dt.option[i - 1] .. ".|cff0000ff" .. d[i] .. "|r", 1, i)
    end
    p:GossipSendMenu(1, p, 60001)
end

function dt.sele(_, p, _, _, i)
    if (i == 0) then
        return dt.show(p)
    else
        local d = dt.data[dt.id]
        if d[6] == i - 1 then
            SendWorldMessage(
                string.format(
                    "|cFFF08000[答题系统]|r恭喜玩家|cffffffff[%s]|r回答正确.奖励|cffff0000%s x %s|r个",
                    p:GetName(),
                    "金币",
                    1
                )
            )
            p:ModifyMoney(10000)
        else
            p:SendBroadcastMessage("|cFFF08000[答题系统]|r很遗憾，回答错误.")
        end
        dt.id = 0
        p:SendBroadcastMessage(
            string.format(
                "|cFFF08000[答题系统]|r答题每|cffff0000%s分钟|r可答一题.下一轮请关注聊天窗口.再接再厉！",
                dt.time
            )
        )
        p:GossipComplete()
    end
end

RegisterPlayerEvent(42, dt.chatcmd)
CreateLuaEvent(dt.reset, dt.time * 60 * 1000, 0)
CreateLuaEvent(dt.remind, dt.time * 60 * 1000 / 4, 0)
RegisterPlayerGossipEvent(60001, 2, dt.sele)
